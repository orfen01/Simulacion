    private void jbtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcularActionPerformed
        op = Integer.parseInt(jTextField2.getText());
        n0 = Integer.parseInt(jTextN0.getText());
        n1 = Integer.parseInt(jTextN1.getText());
        mod = Integer.parseInt(jTextMod.getText());
        veces = Integer.parseInt(jTextField1.getText());
        limpiar();
        switch (op) {
            case 1:
                for (int i = 0; i < veces; i++) {
                    Xn = n0 + n1;
                    Xn = Xn % mod;
                    Un = Xn + n1;
                    Un = Un % mod;
                    Un = dado.nextInt(9);
                    lista.addElement(Un);
                }
                jList1.setModel(lista);
                break;

            case 2:
                for (int i = 0; i < veces; i++) {
                    Xn = n0 - n1;
                    Xn = Xn % mod;
                    Un = Xn - n1;
                    Un = Un % mod;
                    Un = dado.nextInt(9);
                    lista.addElement(Un);
                }
                jList1.setModel(lista);
                break;
            case 3:
                for (int i = 0; i < veces; i++) {
                    Xn = n0 * n1;
                    Xn = Xn % mod;
                    Un = Xn * n1;
                    Un = Un % mod;
                    Un = dado.nextInt(9);
                    lista.addElement(Un);
                }
                jList1.setModel(lista);
    }//GEN-LAST:event_jbtnCalcularActionPerformed

    }

